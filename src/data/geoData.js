export function getStatesForCountry(country) {
  if (!country || country === "*") return [];
  const c = COUNTRIES.find(x => x.name.toLowerCase() === country.toLowerCase());
  return c?.states ?? [];
}

export function getCitiesForState(country, state) {
  if (!country || !state) return [];
  const states = getStatesForCountry(country);
  const s = states.find(x => x.name.toLowerCase() === state.toLowerCase());
  return s?.cities ?? [];
}

export function computeCoverage() {
  return COUNTRIES.map(c => {
    const totalStates = c.states.length;
    const empty = c.states.filter(s => s.cities.length === 0);
    const totalCities = c.states.reduce((sum, s) => sum + s.cities.length, 0);
    let status = "complete";
    if (totalStates === 0) status = "empty";
    else if (empty.length > 0) status = "partial";
    return {
      country: c.name,
      code: c.code,
      totalStates,
      statesWithCities: totalStates - empty.length,
      totalCities,
      emptyStates: empty.map(s => s.name),
      status,
    };
  });
}

export function computeIncompleteCombos() {
  const rows = [];
  for (const c of COUNTRIES) {
    for (const s of c.states) {
      if (s.cities.length === 0) {
        rows.push({
          country: c.name,
          countryCode: c.code,
          state: s.name,
          reason: "No cities catalogued for this state — city dropdown will be empty.",
          fallbackLevel: "state",
          fallbackLevelLabel: "State / region",
        });
      }
    }
    if (c.states.length === 0) {
      rows.push({
        country: c.name,
        countryCode: c.code,
        state: "—",
        reason: "No states catalogued — only country-level matching available.",
        fallbackLevel: "country",
        fallbackLevelLabel: "Country",
      });
    }
  }
  return rows;
}
